import type { DisputeCase, Order, Shipment } from "@/lib/types";

const now = Date.now();
const iso = (offsetHrs: number) =>
  new Date(now + offsetHrs * 36e5).toISOString();

function order(
  partial: Partial<Order> & Pick<Order, "id" | "merchantId" | "merchantName">,
): Order {
  return {
    customerEmail: "ada@hackerhouse.io",
    customerName: "Ada Lovelace",
    itemName: "Sample item",
    itemSku: "SKU-000",
    category: "general",
    amountUsd: 120,
    currency: "USD",
    orderedAt: iso(-72),
    expectedDeliveryAt: iso(-12),
    carrier: "GoFleet",
    trackingNumber: "GF-000000",
    ...partial,
  };
}

function ship(
  status: Shipment["status"],
  events: Shipment["events"] = [],
  trackingNumber = "GF-000",
): Shipment {
  return {
    trackingNumber,
    carrier: "GoFleet",
    status,
    events: events.length
      ? events
      : [
          {
            ts: iso(-72),
            location: "Mississauga, ON",
            status: "Label created",
          },
          {
            ts: iso(-48),
            location: "Toronto, ON",
            status: "Arrived at sort facility",
          },
        ],
    lastUpdate: iso(-24),
  };
}

